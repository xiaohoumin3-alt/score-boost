for q in "${DUPLICATE_QUESTIONS[@]}"; do
  echo "处理: $q"

  # 查询所有匹配的题目ID
  result=$(tcb db nosql execute -c "[{\"TableName\":\"ai_question_pool\",\"CommandType\":\"QUERY\",\"Command\":\"{\\\"find\\\":\\\"ai_question_pool\\\",\\\"filter\\\":{\\\"question\\\":\\\"$q\\\"},\\\"projection\\\":{\\\"_id\\\":1},\\\"limit\\\":10}\"}]" -e $ENV_ID 2>/dev/null)

  # 提取ID（除了第一个，其他都删除）
  ids=$(echo "$result" | grep -o '"_id": "[^"]*"' | head -n -1)

  if [ -n "$ids" ]; then
    echo "  删除重复题: $ids"
  fi
done

echo "=== 完成 ==="
